#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	l;

	if (nb == -2147483648)
	{
		write (1, "-2", 2);
		nb = 147483648;
	}
	if (nb != -2147483648 && nb < 0)
	{
		write (1, "-", 1);
		nb *= -1;
	}
	if (nb < 10)
	{
		l = (nb + '0');
		write (1, &l, 1);
	}
	else
	{
		ft_putnbr(nb / 10);
		l = ((nb % 10) + '0');
		write (1, &l, 1);
	}	
}

